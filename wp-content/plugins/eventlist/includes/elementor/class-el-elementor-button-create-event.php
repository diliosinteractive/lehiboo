	/**
	 * Render button widget output on the frontend.
	 *
	 * Written in PHP and used to generate the final HTML.
	 *
	 * @since 1.0.0
	 * @access protected
	 */
	protected function render() {
		$settings = $this->get_settings_for_display();

		$migrated = isset( $settings['__fa4_migrated']['selected_icon'] );
		$is_new = empty( $settings['icon'] ) && \Elementor\Icons_Manager::is_migration_allowed();

		if ( ! $is_new && empty( $settings['icon_align'] ) ) {
			// @todo: remove when deprecated
			// added as bc in 2.6
			//old default
			$settings['icon_align'] = $this->get_settings( 'icon_align' );
		}

		if( !el_is_vendor() && $settings['display_btn'] == 'login' ) return;

		$url = add_query_arg( ['vendor' => 'create-event'], get_myaccount_page() );

		?>
		<div class="elementor-button-wrapper" >
			<a href="<?php echo esc_url( $url ); ?>" class="elementor-button-link elementor-button elementor-size-<?php echo esc_attr( $settings['size'] ); ?> elementor-animation-<?php echo esc_attr( $settings['hover_animation'] ); ?>" role="button" id="<?php echo esc_attr( $settings['button_css_id'] ); ?>">
				<span class="elementor-button-content-wrapper">
					<?php if ( ! empty( $settings['icon'] ) || ! empty( $settings['selected_icon']['value'] ) ) : ?>
					<span class="elementor-button-icon elementor-align-icon-<?php echo esc_attr( $settings['icon_align'] ); ?>">
						<?php if ( $is_new || $migrated ) :
							\Elementor\Icons_Manager::render_icon( $settings['selected_icon'], [ 'aria-hidden' => 'true' ] );
							else : ?>
								<i class="<?php echo esc_attr( $settings['icon'] ); ?>" aria-hidden="true"></i>
							<?php endif; ?>
						</span>
					<?php endif; ?>
					<span class="elementor-button-text">
						<?php echo esc_html( $settings['text'] ); ?>
					</span>
				</span>
			</a>
		</div>
		<?php
	}

	/**
	 * Render button text.
	 *
	 * Render button widget text.
	 *
	 * @since 1.5.0
	 * @access protected
	 */
	protected function render_text() {
		$settings = $this->get_settings_for_display();

		$migrated = isset( $settings['__fa4_migrated']['selected_icon'] );
		$is_new = empty( $settings['icon'] ) && \Elementor\Icons_Manager::is_migration_allowed();

		if ( ! $is_new && empty( $settings['icon_align'] ) ) {
			// @todo: remove when deprecated
			// added as bc in 2.6
			//old default
			$settings['icon_align'] = $this->get_settings( 'icon_align' );
		}

		$this->add_inline_editing_attributes( 'text', 'none' );
		?>
		<span class="elementor-button-content-wrapper">
			<?php if ( ! empty( $settings['icon'] ) || ! empty( $settings['selected_icon']['value'] ) ) : ?>
				<span class="elementor-button-icon elementor-align-icon-<?php echo esc_attr( $settings['icon_align'] ); ?>">
					<?php if ( $is_new || $migrated ) :
						\Elementor\Icons_Manager::render_icon( $settings['selected_icon'], [ 'aria-hidden' => 'true' ] );
					else : ?>
						<i class="<?php echo esc_attr( $settings['icon'] ); ?>" aria-hidden="true"></i>
					<?php endif; ?>
				</span>
			<?php endif; ?>
			<span class="elementor-button-text">
				<?php echo wp_kses_post( $settings['text'] ); ?>
			</span>
		</span>
		<?php
	}

	
